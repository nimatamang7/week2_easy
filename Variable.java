public class Variable{
    public static void main(String[] args)
{
        int num1 = 5;
        double num2 = 3.14;
        String num3 = "Hello world";

        System.out.println("Integer variable: " + num1);
        System.out.println("Double variable: " + num2);
        System.out.println("String variable: " + num3);
    }
}